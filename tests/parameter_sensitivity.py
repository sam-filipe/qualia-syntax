# ── Comparison utilities ───────────────────────────────────────────────────────

def off_diagonal_vector(matrix: pd.DataFrame) -> np.ndarray:
    """Return the off-diagonal entries (NaN → 0) as a flat vector."""
    filled = matrix.fillna(0).values
    mask = ~np.eye(len(QUALIA), dtype=bool)
    return filled[mask]


def compare_to_baseline(
    matrices: dict[tuple, pd.DataFrame],
    baseline_key: tuple,
) -> pd.DataFrame:
    """
    Compare every non-baseline configuration to the baseline.

    Returns a DataFrame with columns: config, frobenius, pearson_r.
    """
    baseline_vec = off_diagonal_vector(matrices[baseline_key])
    rows = []
    for key in sorted(matrices):
        if key == baseline_key:
            continue
        other_vec = off_diagonal_vector(matrices[key])
        rows.append({
            'config': key,
            'frobenius': np.linalg.norm(baseline_vec - other_vec),
            'pearson_r': np.corrcoef(baseline_vec, other_vec)[0, 1],
        })
    return pd.DataFrame(rows)


